package com.xzy.maven;// created by xzy on 2019-07-27

public class NCCustomerResponse {

    private String name;

    private String pk_customer;

    private String pk_group;

    private String pk_financeorg;

    private String code;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPk_customer() {
        return pk_customer;
    }

    public void setPk_customer(String pk_customer) {
        this.pk_customer = pk_customer;
    }

    public String getPk_group() {
        return pk_group;
    }

    public void setPk_group(String pk_group) {
        this.pk_group = pk_group;
    }

    public String getPk_financeorg() {
        return pk_financeorg;
    }

    public void setPk_financeorg(String pk_financeorg) {
        this.pk_financeorg = pk_financeorg;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

}
