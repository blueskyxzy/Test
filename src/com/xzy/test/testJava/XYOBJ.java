public class XYOBJ {

    private Long X;

    private Long Y;

    public XYOBJ(Long x, Long y) {
        X = x;
        Y = y;
    }

    public Long getX() {
        return X;
    }

    public void setX(Long x) {
        X = x;
    }

    public Long getY() {
        return Y;
    }

    public void setY(Long y) {
        Y = y;
    }
}
